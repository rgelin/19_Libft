#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*res;
	char	*str;

	i = 0;
	str = (char *)s1;
	if (!(res = malloc(sizeof(char) * (ft_strlen(s1) + 1))))
		return (NULL);
	while (str[i])
	{
		res[i] = str[i];
		i++;
	}
	res[i] = 0;
	return (res);
}
